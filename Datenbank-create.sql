ALTER TABLE LIEFERANT ADD PRIMARY KEY (LIEFERANTENNR);
ALTER TABLE BESTELLUNGZUTAT ADD PRIMARY KEY (BESTELLNR,ZUTATENNR);
ALTER TABLE REZEPTINHALT ADD PRIMARY KEY (REZEPTNR,ZUTATENNR);
ALTER TABLE BESTELLTEREZ ADD PRIMARY KEY (BESTELLNR, REZEPTNR);

